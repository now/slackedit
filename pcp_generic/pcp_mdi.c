int MDI_GetCount(HWND hwnd)
{
    HWND hwndMDI;
    int i;

    for (i = 0, hwndMDI = GetWindow(hwnd, GW_CHILD);
        hwndMDI != NULL;
        hwndMDI = GetWindow(hwndMDI, GW_HWNDNEXT)
        )
    {
        if (!GetWindow(hwndMDI, GW_OWNER))
            i++;
    }

    return (i);
}
